                    <p> The Care Quality Commission found, in a recent study, that 34 per cent of care homes had ‘aspects of variable or poor care regarding how the care met people’s mental health, emotional and social needs’.</p>

                    <p>A vital requirement exists for appropriate and effective therapeutic interventions to support those living with dementia in a care home setting.  One such approach is the use of Multi-Sensory Environments. Creating the right sensory environment for those with dementia can help relax the effects of dementia and help not only those with dementia but also the carers who look after them, making their job less stressful and more enjoyable.</p>

                    <p>Creating your sensory room can be harder than it looks but help is out there. From specialist organisations who can help build bespoke sensory rooms, to academic research such as “How to make a sensory room for people living with dementia” – a groundbreaking publication by researcher’s Dr Anke Jakob, from Kingston University London, and Dr Lesley Collier, from the University of Southampton. If you are wanting to explore ways of offering a safe space for those with complex health needs to unwind then read our Blog and full guide for some helpful tips towards creating your own Multi Sensory Environment in your Care Home:</p>

                    <div class="small-subtitle green">How to Create Your Positive Sensory Space</div>

                    <p>Here are some simple but effective ideas on how to create a safe, effective Multi Sensory Environment stimulating all five senses:</p>

                    <p>Sight – Soft lighting is imperative in your sensory room. “LED lighting has a positive impact on resident’s mood and behaviour” (Care UK). Avoid flickering, overhead or harsh lighting this can cause confusion and irritation, but also don’t make the space too dark. Wherever possible try to make use of natural light by softening it through blinds or textiles. Read More in our full report</p>

                    <p>Touch – Planting a bulb in time for spring and watering it creates a meaningful activity or playing with dough and doing the preparation for making a cake. Soft blankets, cushions and toys are comforting and/or soft images to touch.</p>

                    <p>Taste –  Trying different types of fruit allows for a multi-sensory experience of taste, texture and colour.</p>

                    <p>Smell – The gentle aromatherapy of lavender could be used to invoke calmness and memories of spring or having different types of herbs in the room. Regular airing of the room is needed to produce good quality air.</p>

                    <p>Sound – The sound of birdsong and sea waves may be appropriate. Any equipment used should have minimal background noise levels.</p>

                    <p>Movement – a rocking chair, chairs with foot rests, a comfortable place to relax and something to cuddle can provide a pleasurable experience and reduce loneliness. </p>
                    <div class="small-subtitle green">The Multi Sensory Environment for Better Dementia Care</div>

                    <p>Meaningful and enriching therapeutic intervention is a cost-effective way to reduce over-prescribed use of medication and increase happiness and enjoyment in residents with dementia.</p>

                    <p>Multi Sensory Environments can significantly improve the lives of those with dementia and their carer’s by promoting valued interaction between residents, relatives and care staff. When used correctly they foster improved mental health for dementia sufferers and contribute to providing the “safe, effective, compassionate, high quality care” that the Care Quality Commission look for to rate their care homes as outstanding.</p>
